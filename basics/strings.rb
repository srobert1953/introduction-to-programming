# Double quotes Strings, can use interpolation and some special characters
# Use escape charater for double quotes: '\"'.
puts "Hi there, I'm a string"
puts "Want to write \"a quote\""

# Interpolation:
number = 23
puts "Number is: #{number}"

# Single quote Strings
# Use escape char '\' to escape special characters
puts 'Oh yeah, gonna \'burn\' with that much information :O'
